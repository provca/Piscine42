/*
 * Function: ft_ft(int *nbr)
 * --------------------------
 * Updates the value pointed to by `nbr` to 42.
 * 
 * Syntax:
 * void ft_ft(int *nbr)
 * 
 * Arguments:
 * - int *nbr: A pointer to an integer whose value will be set to 42.
 * 
 * Returns:
 * - None
 * 
 * Examples:
 * - int num = 0;
 *   ft_ft(&num); // num is now 42
 */
void	ft_ft(int *nbr)
{
	*nbr = 42;
}
